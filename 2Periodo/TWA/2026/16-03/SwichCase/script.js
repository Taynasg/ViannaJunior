let activity = "GET UP";

switch (activity ) {
    case "Get up":
        console.log("It's 6:30 AM");
        break;
    case "Breakfast":
        console.log("It's 7:00 AM");
        break;
    case "Drive to work":
        console.log("It's 8:00 AM");
        break;
    case "Lunch":
        console.log("It's 12:00 ");
        break;
    default:
        console.log("Activity not registred");
        break;
}