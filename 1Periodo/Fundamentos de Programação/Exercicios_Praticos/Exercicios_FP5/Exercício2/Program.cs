// 2. Fazer um algoritmo que imprima todos os números de 
//100 até 1.

class Program
{
    static void Main(string[] args)
    {
        int numerosImpressos = 10;
        while (numerosImpressos >= 1)
        {
            Console.WriteLine(numerosImpressos);
            numerosImpressos--;
        }
    }
}